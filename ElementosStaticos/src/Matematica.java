
public class Matematica {
// classe que vai subtrair e somar 2 números

	public static int somar(int a, int b) { //método static somar, não precisa instanciar um objeto
		return a + b;  //retorna a soma

	}

	public static int subtrair(int a, int b) { //método static somar, não precisa instanciar um objeto
		return a - b; //retorna a subtração

	}
	
	
	
	
	
	
	
	
	
	
}
